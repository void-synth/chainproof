// Utility Functions
export const utils = {
  // Generate CSS variables from design tokens
  generateCSSVariables: () => {
    const cssVars: Record<string, string> = {};
    
    // Generate color variables
    Object.entries(designSystem.colors).forEach(([colorName, shades]) => {
      Object.entries(shades).forEach(([shade, value]) => {
        cssVars[`--color-${colorName}-${shade}`] = value;
      });
    });

    return cssVars;
  },

  // Responsive breakpoint utilities
  responsive: {
    up: (breakpoint: keyof typeof designSystem.breakpoints) => 
      `@media (min-width: ${designSystem.breakpoints[breakpoint]})`,
    down: (breakpoint: keyof typeof designSystem.breakpoints) => 
      `@media (max-width: ${designSystem.breakpoints[breakpoint]})`,
    between: (min: keyof typeof designSystem.breakpoints, max: keyof typeof designSystem.breakpoints) =>
      `@media (min-width: ${designSystem.breakpoints[min]}) and (max-width: ${designSystem.breakpoints[max]})`,
  },

  // Animation utilities
  animate: (
    element: HTMLElement,
    animation: keyof typeof animations,
    duration: keyof typeof designSystem.animation.duration = 'normal',
    easing: keyof typeof designSystem.animation.easing = 'ease'
  ) => {
    const keyframes = animations[animation];
    const animationDuration = designSystem.animation.duration[duration];
    const animationEasing = designSystem.animation.easing[easing];

    return element.animate(Object.values(keyframes), {
      duration: parseInt(animationDuration),
      easing: animationEasing,
      fill: 'forwards'
    });
  },

  // Color utilities
  color: {
    rgba: (hex: string, alpha: number) => {
      const r = parseInt(hex.slice(1, 3), 16);
      const g = parseInt(hex.slice(3, 5), 16);
      const b = parseInt(hex.slice(5, 7), 16);
      return `rgba(${r}, ${g}, ${b}, ${alpha})`;
    },
    
    lighten: (hex: string, amount: number) => {
      const num = parseInt(hex.slice(1), 16);
      const amt = Math.round(2.55 * amount);
      const R = (num >> 16) + amt;
      const G = (num >> 8 & 0x00FF) + amt;
      const B = (num & 0x0000FF) + amt;
      return `#${(0x1000000 + (R < 255 ? R < 1 ? 0 : R : 255) * 0x10000 +
        (G < 255 ? G < 1 ? 0 : G : 255) * 0x100 +
        (B < 255 ? B < 1 ? 0 : B : 255)).toString(16).slice(1)}`;
    }
  }
}; 